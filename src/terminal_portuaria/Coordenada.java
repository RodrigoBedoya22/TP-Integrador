package terminal_portuaria;


public class Coordenada {
	
	 double coordenadaX;
	 double coordenadaY;
	 
	 public Coordenada(double coordenadaX, double  coordenadaY){
			this.coordenadaX = coordenadaX;
			this.coordenadaY = coordenadaY;
			
	}

	 public Double getX() {
		
		return coordenadaX;
	 }
	 
	  public Double getY() {
			
			return coordenadaY;
		 }

}
